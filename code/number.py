#!/usr/bin/env python3

x = 3
print(type(x))      # Print "<class 'int'>"
print(x)            # Print "3"
print(x + 1)        # Addition; print "4"
print(x - 1)        # Substraction; print "2"
print(x * 2)        # Multiplication; Print "6"
print(x ** 2)       # Exponential; print "9"

x += 1
print(x)            # Print "4"
x *= 2              # Print "8"
print(x)            # Print "6"
y = 2.5
print(type(y))      # Prints "<class 'float'>"
print(y, y + 1, y * 2, y ** 2)
